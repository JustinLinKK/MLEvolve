"""Build run_chunk_examples.py — embeds every chunk's example_code as a flat
Python dict literal so the runner has no yaml dependency.

Why: Pod image is vanilla pytorch — no PyYAML guaranteed, no repo mount.
We bake everything into one file, ship it via ConfigMap.
"""
from __future__ import annotations
import json
from pathlib import Path

import yaml

REPO = Path(__file__).resolve().parent.parent.parent
CHUNK_DIR = REPO / "schema" / "code_doc_chunks"
OUT = Path(__file__).resolve().parent / "run_chunk_examples.py"


TEMPLATE = '''"""Auto-generated by build_runner.py. Runs each chunk example in an
isolated subprocess (60s timeout). Prints structured JSON at the end so
kubectl logs is parseable."""
import json
import subprocess
import sys
import tempfile
import time

CHUNKS = __CHUNKS__


def run_one(cid, code):
    if not code.strip():
        return {"chunk_id": cid, "status": "SKIP_EMPTY", "exit": None,
                "duration_s": 0.0, "stdout": "", "stderr": ""}
    with tempfile.NamedTemporaryFile("w", suffix=".py", delete=False) as f:
        f.write(code)
        path = f.name
    t0 = time.time()
    try:
        cp = subprocess.run(
            [sys.executable, path],
            timeout=60, capture_output=True, text=True,
        )
        dt = time.time() - t0
        return {"chunk_id": cid, "status": "RAN", "exit": cp.returncode,
                "duration_s": round(dt, 2),
                "stdout": cp.stdout[-2000:], "stderr": cp.stderr[-2000:]}
    except subprocess.TimeoutExpired:
        return {"chunk_id": cid, "status": "TIMEOUT", "exit": -1,
                "duration_s": 60.0, "stdout": "", "stderr": "timeout"}
    except Exception as exc:
        return {"chunk_id": cid, "status": "ERROR", "exit": -2,
                "duration_s": round(time.time() - t0, 2),
                "stdout": "", "stderr": repr(exc)}


def main():
    try:
        import torch
        env = {"torch": str(torch.__version__),
               "cuda": torch.cuda.is_available(),
               "device_name": (torch.cuda.get_device_name(0)
                              if torch.cuda.is_available() else None),
               "cc": (list(torch.cuda.get_device_capability())
                      if torch.cuda.is_available() else None)}
    except Exception as exc:
        env = {"torch_import_error": repr(exc)}
    print("=== ENV ===")
    print(json.dumps(env, indent=2))
    print("=== RUNNING {} CHUNK EXAMPLES ===".format(len(CHUNKS)))
    results = []
    for cid, code in CHUNKS:
        print("\\n--- {} ---".format(cid), flush=True)
        r = run_one(cid, code)
        print("  status={} exit={} dur={}s".format(
            r["status"], r["exit"], r["duration_s"]), flush=True)
        if r["stdout"]:
            print("  stdout(tail): " + r["stdout"].rstrip()[-400:])
        if r["stderr"]:
            print("  stderr(tail): " + r["stderr"].rstrip()[-400:])
        results.append(r)
    print("\\n=== RESULT_JSON_BEGIN ===")
    print(json.dumps({"env": env, "results": results}, indent=2))
    print("=== RESULT_JSON_END ===")
    return 0


if __name__ == "__main__":
    sys.exit(main())
'''


def main() -> int:
    chunks: list[tuple[str, str]] = []
    for p in sorted(CHUNK_DIR.glob("*.yaml")):
        d = yaml.safe_load(p.read_text())
        chunks.append((d["chunk_id"], d.get("example_code") or ""))
    print(f"loaded {len(chunks)} chunks from {CHUNK_DIR}")
    # Build chunk list as a Python literal via json (safe for any quotes)
    chunk_literal = "[\n    " + ",\n    ".join(
        f"({json.dumps(cid)}, {json.dumps(code)})" for cid, code in chunks
    ) + ",\n]"
    runner = TEMPLATE.replace("__CHUNKS__", chunk_literal)
    OUT.write_text(runner)
    print(f"wrote {OUT} ({len(runner)} bytes, {len(chunks)} chunks)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
